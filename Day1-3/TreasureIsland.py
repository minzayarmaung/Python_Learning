print("Welcome to Treasure Island.\n")
print("Your Mission is to find the Treasure\n")

direction = input("Left or Right?\n")
direction_lowercase = direction.lower()

if direction_lowercase == "right":
    print("You fell into a Hole. Game Over !")
    
elif direction_lowercase == "left":
    choose = input("Swim or Wait ?\n")
    choose_lower_case = choose.lower()
    
    if choose_lower_case == "swim":
        print("You downed. Game Over !")
        
    elif choose_lower_case == "wait":
        door = input("Which Door : Red , Blue , Yellow ?\n")
        door_lowercase = door.lower()
        
        if door_lowercase == "yellow":
            print("You Found the Treasure Box !")
        else:
            print("You entered the room with the traps. Game Over !")