piano_keys = ("a", "b", "c", "d", "e", "f", "g")


# Slicing The Tuple
print(piano_keys[2:5])

# Getting all in the Tuple
print(piano_keys[::])

# Getting items in the Tuple with the ending point
print(piano_keys[:5])

# Reversing the Tuple
print(piano_keys[::-1])

# Getting items but jumping over for 2 items
print(piano_keys[::2])
